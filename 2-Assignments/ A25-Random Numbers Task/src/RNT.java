public class RNT {

    public static void randomsthings(int first, int second) {
        System.out.println("Here are two random numbers:\n"+first+" and "+second);//prints out first two variables

        System.out.println("\nthe smallest is "+Math.min(first,second));//compares for smaller value
        System.out.println("the biggest is "+Math.max(second,first)+"\n");//compares for largest value
    }}
